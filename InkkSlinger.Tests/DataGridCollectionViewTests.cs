    [Fact]
    public void DataGrid_AutoGeneratingColumn_CanCancelGeneratedColumn()
    {
        var source = new ObservableCollection<Row>
        {
            new(1, "Alpha")
        };

        var grid = new DataGrid();
        var autoGeneratedRaised = false;
        grid.AutoGeneratingColumn += static (_, args) =>
        {
            if (args.PropertyName == nameof(Row.Name))
            {
                args.Cancel = true;
            }
        };
        grid.AutoGeneratedColumns += (_, _) => autoGeneratedRaised = true;
        grid.ItemsSource = source;

        grid.Measure(new Microsoft.Xna.Framework.Vector2(600, 400));
        grid.Arrange(new LayoutRect(0, 0, 600, 400));

        Assert.True(autoGeneratedRaised);
        Assert.Equal(new[] { nameof(Row.Id) }, grid.ColumnHeadersForTesting.Select(static header => header.GetContentText()));
    }

    private sealed record Row(int Id, string Name);

    private sealed record OtherRow(string Code, bool Enabled);

    private sealed class ObservableRow : INotifyPropertyChanged
    {
        private string _name;

        public ObservableRow(int id, string name)
        {
            Id = id;
            _name = name;
        }

        public event PropertyChangedEventHandler? PropertyChanged;

        public int Id { get; }

        public string Name
        {
            get => _name;
            set
            {
                if (_name == value)
                {
                    return;
                }

                _name = value;
                PropertyChanged?.Invoke(this, new PropertyChangedEventArgs(nameof(Name)));
            }
        }
    }

    private static Microsoft.Xna.Framework.Vector2 GetCenter(LayoutRect rect)
    {
        return new Microsoft.Xna.Framework.Vector2(rect.X + (rect.Width / 2f), rect.Y + (rect.Height / 2f));
    }
}
